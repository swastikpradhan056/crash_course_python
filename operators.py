num1 = 10
num2 = 2

# Arithmetic Operators

print("num1 + num2 is", num1 + num2)
print("num1 - num2 is", num1 - num2)
print("num1 * num2 is", num1 * num2)
print("num1 / num2 is", num1 / num2)

# Assignment Operators
a = 4
a += 2
print(a)

# Comparison Operators
x = 8
y = 3
print(x > y)
print(x < y)
print(x != y)

# Logical Operators

print(x == y and x < y)
print(x == y or x < y)